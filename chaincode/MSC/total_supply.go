func		totalSupply() (string, error) {
	var	err		error
	var	amount	[]byte

	amount, err = STUB.GetState("total_supply")
	if err != nil {
		return "", fmt.Errorf("Cannot get total supply: %s", err)
	}

	LOG.Info("Successfull call to totalSupply") // LOG
	return string(amount), nil
}
